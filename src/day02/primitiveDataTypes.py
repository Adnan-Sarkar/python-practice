# Primitive Data Types

# String
name = "Mango"
print(name)
print(name[0])
print(name[-1])

# Integer
age = 26
bigNumber = 123_456_789
print(age)
print(age + 10)
print(bigNumber)

# Float
cgpa = 3.5
pi = 3.14159
print(cgpa)
print(cgpa + 2)
print(pi)

# Boolean
isOkk = True
isRaining = False
print(isOkk)
print(isRaining)